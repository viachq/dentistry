def main() -> None:
    db = SessionLocal()
    try:
        # Update categories for existing services
        for name, category in CATEGORY_MAP.items():
            svc = db.query(Service).filter(Service.name == name).first()
            if svc:
                svc.category = category
                print(f"  Updated category for: {name}")

        # Add new services if they don't exist
        added = 0
        for name, category, desc, duration, price in EXTRA_SERVICES:
            if not db.query(Service).filter(Service.name == name).first():
                db.add(Service(name=name, category=category, description=desc, duration_minutes=duration, price=price))
                added += 1
                print(f"  Added: {name}")

        db.commit()
        print(f"Done. Added {added} new services.")
    finally:
        db.close()


if __name__ == "__main__":
    main()
